	/**
	 * @param aDoc
	 * @throws javax.xml.xpath.XPathExpressionException
	 */
	public void buildTabsGUI(Document aDoc) throws Exception {
		Document baseTemplateInfoDoc = SystemConfig.getBaseTemplateInfoDoc();
		NodeList baseTemplateInfoChildNodes = baseTemplateInfoDoc.getFirstChild().getChildNodes();
		for (int i = 0; i < baseTemplateInfoChildNodes.getLength(); i++) {
			Node paramsClassesTypeNode = baseTemplateInfoChildNodes.item(i);
			if (paramsClassesTypeNode.getNodeType() == Node.ELEMENT_NODE) {
				JPanel contentPanel = new JPanel();
				contentPanel.setLayout(new BoxLayout(contentPanel, BoxLayout.PAGE_AXIS));
				tabs.add(((Element) paramsClassesTypeNode).getAttribute(SystemConfig.NAME_ATTRIBUTE_NAME),
						new JScrollPane(contentPanel));

				for (int j = 0; j < 15; j++) {
					JPanel subPanel = new JPanel();
					GroupLayout subLayout = new GroupLayout(subPanel);
					subPanel.setLayout(subLayout);
					subLayout.setAutoCreateGaps(true);
				}
			}
		}

		for (int i = 0; i < tabPaths.length; i++) {
			XPathExpression xpath = XPathFactory.newInstance().newXPath().compile(tabPaths[i]);
			NodeList nodeList = (NodeList) xpath.evaluate(aDoc, XPathConstants.NODESET);
			Node tab = nodeList.item(0);

			if (tab == null) {
				continue;
			}

			JPanel contentPanel = new JPanel();
			contentPanel.setLayout(new BoxLayout(contentPanel, BoxLayout.PAGE_AXIS));
			tabs.add(((Element) tab).getAttribute(SystemConfig.NAME_ATTRIBUTE_NAME), new JScrollPane(contentPanel));

			String directory = ((Element) tab).getAttribute(SystemConfig.TEMPLATE_DIRECTORY_ATTRIBUTE_NAME);

			if (!tab.hasChildNodes()) {
				continue;
			}
			NodeList params = tab.getChildNodes();
			for (int k = 0; k < params.getLength(); k++) {
				Node param = params.item(k);
				if (param.getNodeType() == Node.ELEMENT_NODE) {
					JPanel subPanel = new JPanel();
					GroupLayout subLayout = new GroupLayout(subPanel);
					subPanel.setLayout(subLayout);
					subLayout.setAutoCreateGaps(true);
					JTextField nameField = new JTextField(((Element) param).getAttribute("name"));
					JTextField pathField = new JTextField(param.getTextContent());

					JButton button = new JButton("Import");
					button.addActionListener(new ImportFileButtonListener(directory, pathField));
					pathField.setMaximumSize(new Dimension(Integer.MAX_VALUE, pathField.getPreferredSize().height));
					subLayout.setHorizontalGroup(subLayout.createSequentialGroup().addComponent(nameField)
							.addComponent(pathField).addComponent(button));
					subLayout.setVerticalGroup(subLayout.createSequentialGroup()
							.addGroup(subLayout.createParallelGroup(GroupLayout.Alignment.BASELINE)
									.addComponent(nameField).addComponent(pathField).addComponent(button)));

					paramsTextFields.add(pathField);
					contentPanel.add(subPanel);
				}
			}
		}
	}
