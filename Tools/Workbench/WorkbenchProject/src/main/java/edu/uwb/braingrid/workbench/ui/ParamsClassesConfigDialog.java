	private void buildButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_buildButtonActionPerformed
		try {
			// gather param values
			ArrayList<String> paramStrs = new ArrayList<>();
			for (int i = 0; i < paramsTextFields.size(); i++) {
				paramStrs.add(paramsTextFields.get(i).getText());
			}
			// create the file we want to save
			File file = new File(SystemConfig.getAllParamsClassesFilePath());
			// create any necessary non-existent directories
			new File(file.getParent()).mkdirs();
			file.createNewFile();
			// write the content into xml file
			Transformer t;
			t = TransformerFactory.newInstance().newTransformer();
			t.setOutputProperty(OutputKeys.INDENT, "yes");
			t.setOutputProperty("{http://xml.apache.org/xslt}indent-amount", "3");
			t.transform(new DOMSource(xmlDoc), new StreamResult(file));
			messageLabelText
					.setText("<html><span style=\"color:green\">" + "Classes successfully persisted...</span></html>");
		} catch (Exception ex) {
			Logger.getLogger(ParamsClassesConfigDialog.class.getName()).log(Level.SEVERE, null, ex);
		}
	}// GEN-LAST:event_buildButtonActionPerformed

